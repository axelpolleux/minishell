#include "libftprintf.h"

void	ft_putnbr(long n, int *len)
{
	if (n < 0)
	{
		ft_putchar('-', len);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10, len);
	ft_putchar(n % 10 + '0', len);
}
